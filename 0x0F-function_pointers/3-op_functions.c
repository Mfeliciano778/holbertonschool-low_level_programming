#include "3-calc.h"
#include <stdlib.h>
#include <stdio.h>

/**
 * op_add - function adds to integers
 * @a: first integer were adding
 * @b: second integer were adding
 *
 * Return: Always 0
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - function subtracts two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: Always 0
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - function multiplies two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: Always 0
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - function divides two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: Always 0
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - function defines the modulo of two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: Always 0
 */
int op_mod(int a, int b)
{
	return (a % b);
}
